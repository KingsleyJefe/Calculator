function add(x, y) {
    return x + y;
};

function subtract(x, y) {
    return x - y;
}

function multiply(x, y) {
    return x * y;
}

function divide(x, y) {
    return x / y;
}

function absoluteValue(x) {
    return Math.abs(x);
}

function cosine(x) {
    return Math.cos(x);
}

function floorValue(x) {
    return Math.floor(x);
}

function ceilingValue(x) {
    return Math.ceiling(x);
}

function tangent(x) {
    return Math.tan(x);
}

function exponentialValue(x) {
    return Math.exp(x);
}

function power(x, y) {
    return Math.pow(x, y);
}




